public class TelegramHandler {
    private String data;

    public TelegramHandler() {
        this.data = data;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    @Override
    public String toString() {
        return "TelegramHandler{" +
                "data='" + data + '\'' +
                '}';
    }
}
